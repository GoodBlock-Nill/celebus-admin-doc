import type { RankingUser } from '@/lib/types';

function withPmSt(base: Omit<RankingUser, 'pmAccumulatedGP' | 'pmParticipationCount' | 'pmWinRate' | 'stAccumulatedGP' | 'stParticipationCount' | 'stWinRate'>): RankingUser {
  const stGP = Math.round(base.accumulatedGP * 0.15);
  const pmGP = base.accumulatedGP - stGP;
  const stParticipation = Math.round(base.participationCount * 0.2);
  const pmParticipation = base.participationCount - stParticipation;
  return {
    ...base,
    pmAccumulatedGP: pmGP,
    pmParticipationCount: pmParticipation,
    pmWinRate: +(base.winRate + 0.05).toFixed(3),
    stAccumulatedGP: stGP,
    stParticipationCount: stParticipation,
    stWinRate: +(Math.max(0, base.winRate - 0.1)).toFixed(3),
  };
}

